#include "iostream"
using namespace std;

int main() {
  int num = 13;
  bool myBool = true;

  cout << "My number is: " << num << endl;
  cout << "true is represented as: " << myBool << endl;

  long num2 = 3000000000;
  bool myBool2 = false;

  cout << "My number2 is: " << num2 << endl;
  cout << "false is represented as: " << myBool2 << endl;

  return 0;
}